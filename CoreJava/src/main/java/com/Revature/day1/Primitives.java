package com.Revature.day1;

public class Primitives {

	public static void main(String[] args) {
		System.out.println("The primitives are as follows: ");
		byte b = 5;
		System.out.println("Byte is a single byte and b =  " + b);
		
		short s = 10;
		System.out.println("Short is 2 bytes signed and s = " + s);
		
		char c = 94;
		System.out.println("char is a 2 byte number unsigned and c = " + c);
		
		int i = 10000;
		System.out.println("Int is a 4 byte number and i = " + i);
		
		long l = 20000000000l;
		System.out.println("Long is a 8 byte number and l = " + l);
		
		float f = 5.2f;
		System.out.println("float is a decimal number of 4 bytes and f = " + f);
		
		double dub =  5.2;
		System.out.println("Double is a decimal number of 8 bytes and dub = " + dub);
		
		boolean bool = true; // or false
		System.out.println("Boolean is either true or false and bool = " + bool);

	}

}
